class TotoroSketch {
    constructor() {
        this.type = "sketch";
    }

    render() {
        let triangleCount = totoTris.length;
        let rgba;

        for (let i = 0; i < triangleCount; i++) {
            rgba = totoTris[i][6];

            gl.uniform4f(u_FragColor, rgba[0] / 255, rgba[1] / 255, rgba[2] / 255, rgba[3] / 255);

            drawTriangle([totoTris[i][0] / 20 - 0.5, totoTris[i][1] / 20 + 0.5,
            totoTris[i][2] / 20 - 0.5, totoTris[i][3] / 20 + 0.5,
            totoTris[i][4] / 20 - 0.5, totoTris[i][5] / 20 + 0.5])
        }
    }
}

